package Exercicios.Exercicios_Fixacao2.Ex4;

public class Reta {
    private double coeAngular;
    private double coeLinear;
    private int ponto1;
    private int ponto2;

    public double getCoeAngular() {
        return coeAngular;
    }

    public void setCoeAngular(double coeAngular) {
        this.coeAngular = coeAngular;
    }

    public double getCoeLinear() {
        return coeLinear;
    }

    public void setCoeLinear(double coeLinear) {
        this.coeLinear = coeLinear;
    }

    public int getPonto1() {
        return ponto1;
    }

    public void setPonto1(int ponto1) {
        this.ponto1 = ponto1;
    }

    public int getPonto2() {
        return ponto2;
    }

    public void setPonto2(int ponto2) {
        this.ponto2 = ponto2;
    }

}
