package Exercicios.Exercicios_Fixacao2.Ex1;

public class Contador {
    private int valor = 10;

    public int getValor() {
        return valor;
    }

    public void zerar(){
        valor = 0;
    }
    public void incrementar(){
        valor++;
    }
    public int retornarValor(){
        return valor = 10;
    }
}
